def _env_bool(name, default):
    value = os.getenv(name)

    if value is None:
        return default

    return value.strip().lower() not in ("0", "false", "no", "off")


def _env_float(name, default):
    value = os.getenv(name)

    if value is None:
        return default

    try:
        return float(value)
    except ValueError:
        return default


def _env_int(name, default):
    value = os.getenv(name)

    if value is None:
        return default

    try:
        return int(value)
    except ValueError:
        return default


